    private class LinkerListRenderer<E> extends JPanel implements ListCellRenderer<E> {

        @Override
        public Component getListCellRendererComponent(
                JList<? extends E> list, E value, int index,
                boolean isSelected, boolean cellHasFocus) {

            super.setLayout(new BorderLayout());
            super.setOpaque(true);
            super.removeAll();

            if (value instanceof HexEditorArea.LinkASM) {
                HexEditorArea.LinkASM link = (HexEditorArea.LinkASM) value;
                //label
                JLabel label = new JLabel("<html> <font color=BLUE>" + link.oldStr + "</font> >> <font color=GREEN>0x" + link.newStr+"</font></html>");
                label.setFont(list.getFont());
                
                if (isSelected) {
                    label.setForeground(Color.red);
                }
                super.add(label, BorderLayout.CENTER);
            }

            return this;
        }

    }

